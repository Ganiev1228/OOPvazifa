public class Point {

	int x,y,z;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}
	public void kordinatalar () {
		System.out.print("X="+x+"\nY="+y+"\nZ="+z);
	}
}
