package oopVazifa;

public class Car {

	String rangi;
	String modeli;
	int narxi;
	String tuzilma;

	public String getRangi() {
		return rangi;
	}

	public void setRangi(String rangi) {
		this.rangi = rangi;
	}

	public String getModeli() {
		return modeli;
	}

	public void setModeli(String modeli) {
		this.modeli = modeli;
	}

	public int getNarxi() {
		return narxi;
	}

	public void setNarxi(int narxi) {
		this.narxi = narxi;
	}

	public String getTuzilma() {
		return tuzilma;
	}

	public void setTuzilma(String tuzilma) {
		this.tuzilma = tuzilma;
	}

}
